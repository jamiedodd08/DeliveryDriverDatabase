DROP DATABASE IF EXISTS DeliveryDB;

CREATE DATABASE DeliveryDB;

USE DeliveryDB;

CREATE TABLE Drivers(
DriverID int(11) NOT NULL AUTO_INCREMENT,
FirstName varchar(30) NOT NULL,
LastName varchar(30) NOT NULL,
Gender varchar(10) NOT NULL,
DOB date NOT NULL,
AddressLine1 varchar(30) NOT NULL,
AddressLine2 varchar(30) NOT NULL,
Postcode varchar(8) NOT NULL,
PhoneNumber varchar(11) NOT NULL,
PRIMARY KEY (DriverID)
)AUTO_INCREMENT=1;

CREATE TABLE Vehicles(
Registration varchar(8) PRIMARY KEY NOT NULL,
Make varchar(20) NOT NULL,
Model varchar(20) NOT NULL
);

CREATE TABLE Shifts(
ShiftID int(11) NOT NULL AUTO_INCREMENT,
DriverID int(11) NOT NULL,
Registration varchar(8) NOT NULL,
ShiftDate date NOT NULL,
ShiftType varchar(30) NOT NULL,
PRIMARY KEY (ShiftID),
FOREIGN KEY (DriverID) REFERENCES Drivers (DriverID),
FOREIGN KEY (Registration) REFERENCES Vehicles (Registration)
)AUTO_INCREMENT=1;

CREATE TABLE GPS(
GPSID int PRIMARY KEY AUTO_INCREMENT,
Registration varchar(8) NOT NULL,
Latitude decimal (10,8) NOT NULL,
Longitude decimal (11,8) NOT NULL,
TimeDate datetime(6) NOT NULL,
FOREIGN KEY (Registration) REFERENCES Vehicles (Registration)
)AUTO_INCREMENT=1;

CREATE TABLE Customers(
CustomerID int(11) NOT NULL AUTO_INCREMENT,
FirstName varchar(30) NOT NULL,
LastName varchar(30) NOT NULL,
DOB date NOT NULL,
AddressLine1 varchar(30) NOT NULL,
AddressLine2 varchar(30) NOT NULL, 
Postcode varchar(8) NOT NULL,
PhoneNumber varchar(11) NOT NULL,
PRIMARY KEY (CustomerID)
)AUTO_INCREMENT=1;

CREATE TABLE Parcels(
ParcelID int(11) NOT NULL AUTO_INCREMENT,
CustomerID int(11) NOT NULL,
ShiftID int(11) NOT NULL,
ParcelWeight int(20) NOT NULL,
ParcelSize varchar(30) NOT NULL,
Status varchar(30) NOT NULL,
TimeDate datetime(6) NOT NULL,
PRIMARY KEY (ParcelID),
FOREIGN KEY (CustomerID) REFERENCES Customers (CustomerID),
FOREIGN KEY (ShiftID) REFERENCES Shifts (ShiftID)
)AUTO_INCREMENT=1;

